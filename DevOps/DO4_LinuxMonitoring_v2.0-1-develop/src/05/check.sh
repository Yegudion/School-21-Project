#!/bin/bash

function check_input() {

    #0. Проверка кол-ва параметров
    if [ "$#" -ne 1 ]; then
        echo "Ошибка: неверное количество аргументов." >&2
        exit 1
    fi

    #1. Проверка параметров ввода
    if ! [ "$1" -eq 1 ] && ! [ "$1" -eq 2 ] && ! [ "$1" -eq 3 ] && ! [ "$1" -eq 4 ]; then
        echo "Число не равно 1, 2, 3 или 4"
        exit 1
    fi
}
#!/bin/bash

function check_input() {

    #0. Проверка на кол-во аргументов
    if [ "$#" -ne 1 ]; then
        echo "Ошибка: неверное количество аргументов. Используйте 1 аргумент" >&2
        exit 1
    fi

    #1. Проверка чисел
    if ! [ "$2" -eq 1 ] && ! [ "$2" -eq 2 ] && ! [ "$2" -eq 3 ]; then
        echo "Число не равно 1, 2 или 3" >&2
        exit 1
    fi
}
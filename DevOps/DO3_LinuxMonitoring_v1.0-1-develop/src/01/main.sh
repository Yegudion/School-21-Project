#!/bin/bash

if [ -z "$1" ]; then
    echo "Параметр не введен"
    exit 1
fi

if [ $# -ne 1 ]; then
    echo "Слишком много параметров"
    ecit 1
fi

str1=$(echo $1 | grep -e '[0-9]') #строка содержит только цифры в строке
str2=$(echo $1 | grep -e '[^0-9]') #строка содержит тольок не цифры строке

if [[ "$str1" != "" ]] && [[ "$str2" == "" ]]; then
    echo "Введен НЕВЕРНЫЙ параметр(число): $1"  
else 
    echo "Введен ВЕРНЫЙ параметр: $1"
fi
